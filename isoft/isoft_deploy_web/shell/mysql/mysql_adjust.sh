#!/bin/bash

###################################################################################################################
# 从配置文件中读取 root 密码
rootPwd=$(cat /var/shell/mysql/rootPwd.txt)
echo $rootPwd
###################################################################################################################

# 不能立刻执行 mysql_modify 操作,需要等待一段时间,等待容器完全创建,否则会报错
sleep 60

# 此处的参数传递只能使用单引号,外部使用双引号,否则无法传入
docker exec mysql /bin/bash -c "sh /var/shell/mysql/mysql_modify.sh '$rootPwd'"